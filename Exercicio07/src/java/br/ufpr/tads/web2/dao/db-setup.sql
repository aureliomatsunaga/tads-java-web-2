-- Apagar esquemas anteriores
DROP TABLE IF EXISTS tb_cliente;
DROP TABLE IF EXISTS tb_cidade;
DROP TABLE IF EXISTS tb_estado;
DROP TABLE IF EXISTS tb_usuario;


-- Criar tabela de usuários
CREATE TABLE tb_usuario(
  id_usuario SERIAL PRIMARY KEY,
  nome_usuario VARCHAR(100) NOT NULL,
  login_usuario VARCHAR(50) NOT NULL UNIQUE,
  senha_usuario VARCHAR(50) NOT NULL
);


-- Criar tabela de estados
CREATE TABLE tb_estado(
  id_estado SERIAL PRIMARY KEY,
  nome_estado VARCHAR(30) NOT NULL,
  sigla_estado CHAR(2) NOT NULL
);


-- Criar tabela de cidades
CREATE TABLE tb_cidade(
  id_cidade SERIAL PRIMARY KEY,
  nome_cidade VARCHAR(100) NOT NULL,
  estado_id INT NOT NULL REFERENCES tb_estado(id_estado)
);


-- Criar tabela de clientes
CREATE TABLE tb_cliente(
  id_cliente SERIAL PRIMARY KEY,
  cpf_cliente CHAR(11) NOT NULL UNIQUE,
  nome_cliente VARCHAR(100) NOT NULL,
  email_cliente VARCHAR(100) NOT NULL UNIQUE,
  data_cliente DATE NOT NULL,
  cep_cliente CHAR(8),
  rua_cliente VARCHAR(100),
  nr_cliente INT,
  cidade_id INT REFERENCES tb_cidade(id_cidade)
);


